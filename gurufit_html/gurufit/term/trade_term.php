						<ol>
							<li>이용자는 보유 중인 쿠폰의 환급을 회사에 요구할 경우 쿠폰 전액을 환급 받을 수 있습니다. 단, 만료일이 지난 쿠폰의 경우 일부 또는 전부가 환급되지 않을 수 있습니다.</li>
							<li>이용자가 상품 구매나 이벤트 등을 통하여 회사로부터 무상 제공받은 쿠폰의 경우 회사에서 정한 기한 및 이용방법에 한해서만 이용 가능하며, 기한 및 이용방법은 쿠폰 발행시의 공지내용 및 홈페이지 내 고객센터에서 확인할 수 있습니다. </li>
