						<ol>
							<li>이용자가 결제대금예치서비스를 이용한 경우, 이용자는 거래 지시된 금액의 정보가 수취인이 지정한 전자적 장치에 도달한 때까지 거래 지시를 철회할 수 있습니다.</li>
							<li>회사는 이용자의 거래지시의 철회에 따라 지급거래가 이루어지지 않은 경우 수령한 자금을 이용자에게 반환하여야 합니다. </li>
						</ol>
