							<li>이용자의 구매 취소 등의 사유 발생으로 회사가 이용자에게 기부여한 쿠폰을 환수하고자 하는 경우 당해 쿠폰 환수 시점에 당해 이용자의 쿠폰 잔액이 환수쿠폰 보다 작을 경우에는 회사는 당해 이용자는 자신의 마이너스 쿠폰을 이용자의 추가 구매를 통한 쿠폰 적립, 현금 결제를 통하여 만회할 수 있습니다. </li>
