						<p>제 2장 결제대행서비스 제 16조는 본 장 결제대금예치서비스에 준용합니다.</p>
						<h3 class="term_s_title term_center">제4장   선불전자지급수단(쿠폰)</h3>
						<h4 class="term_s_title">제22조 (정의)</h4>
						<p>본 장에서 정하는 용어의 정의는 다음과 같습니다.</p>
						<ol>
							<li>‘선불전자지급수단’은 ‘쿠폰’과 같이 재화 등을 구매하고 그 가를 지급하는데 사용하기 위하여 회사가 미리 이용자에게 공지하고 발행한 결제수단으로, 전자금융거래법에서 규정하고 있는 것을 말합니다.</li>
							<li>‘쿠폰’이라 함은 회사의 몰에서 사용할 수 있는 선불전자지급수단을 말합니다. 다만 제 25조의 2에 따라 이용이 제한될 수 있습니다.</li>
							<li>‘이용자’라 함은 본 약관에 동의하고 판매자로부터 재화 등을 구매하고 ‘쿠폰’을 결제수단으로 하여 그 대가를 지급하는 자를 말합니다.</li>
							<li>‘판매자’라 함은 이용자에게 재화 등을 판매하고 ‘쿠폰’을 결제수단으로 하여 그 대가를 지급받는 자를 말합니다.</li>
							<li>본 장에서 ‘접근매체’라 함은 ‘쿠폰’을 이용한 전자금융거래에 있어서 지급지시를 하거나 이용자 및 거래내용의 진실성과 정확성을 확보하기 위하여 회사에 등록된 이용자번호 및 비밀번호 기타 회사가 지정한 수단을 말합니다.</li>
						</ol>	
						<h4 class="term_s_title">제23조 (접근매체의 관리)</h4>
						<ol>
							<li>회사는 이용자로부터 쿠폰이나 접근매체의 분실 또는 도난 등의 통지를 받기 전에 발생한 저장된 금액에 대한 손해에 대해서는 책임지지 않습니다.</li>
							<li>제2장 결제대행서비스 제 16조 1항 내지 제3항은 본 장 선불 전자지급수단(쿠폰)에 준용합니다. </li>
						</ol>	
