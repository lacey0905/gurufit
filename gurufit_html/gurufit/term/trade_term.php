							<li>이용자는 회사의 분쟁처리결과에 이의가 있으면 ‘금융감독기구의 설치 등에 관한 법률’ 제 51조의 규정에 따른 금융감독원의 금융분쟁조정위원회나 ‘소비자기본법’ 제 31조 제 1항의 규정을 따른 한국소비자원의 소비자분쟁조정위원회에 ‘회사’의 ‘전자금융거래서비스’의 이용과 관련한 분쟁조정을 신청할 수 있습니다.</li>
						</ol>
						<h4 class="term_s_title">제11조 (회사의 안전성 확보 의무)</h4>
						<p>회사는 전자금융거래가 안전성과 신뢰성을 확보할 수 있도록 전자금융거래의 종류별로 전자적 전송이나 처리를 위한 인력, 시설, 전자적 장치 등의 정보기술부문 및 전자금융업무에 관하여 금융위원회가 정하는 기준을 준수합니다.</p>
						<h4 class="term_s_title">제12조 (약관 외 준칙)</h4>
						<p>본 약관에서 정하지 아니한 사항(용어의 정의 포함)에 대하여는 전자금융거래법, 전자상거래 등에서의 ‘소비자 보호에 관한 법률’, ‘통신판매에 관한 법률’, ‘여신전문금융업법’ 등 소비자보호 관련 법령 및 개별약관에서 정한 바에 따릅니다.</p>
						<h4 class="term_s_title">제13조 (관할)</h4>
						<p>회사와 이용자 간에 발생한 분쟁에 관한 관할은 민사소송법에서 정한 바를 따릅니다.</p>
						<h3 class="term_s_title term_center">제2장   전자지급결제대행 서비스</h3>
						<h4 class="term_s_title">제１4조 (정의)</h4>
						<p>본 장에서 정하는 용어의 정의는 다음과 같습니다.</p>
						<ol>
							<li>‘전자지급결제대행 서비스’란 전자적 방법으로 재화 또는 용역(이하 ‘재화 등’)의 구매에 있어서 지급결제정보를 송신하거나 수신하는 것 또는 그 대가의 정산을 대행하거나 매개하는 서비스를 말합니다.</li>
							<li>‘이용자’란 본 약관에 동의하고 회사가 제공하는 전자지급결제대행 서비스를 이용하는 자를 말합니다.</li>
						</ol>
						<h4 class="term_s_title">제１5조 (거래지시 철회)</h4>
						<ol>
							<li>이용자가 전자지급결제대행 서비스를 이용한 경우, 이용자는 거래지시 된 금액의 정보에 대하여 수취인의 계좌가 개설되어 있는 금융기관 또는 회사의 계좌의 원장에의 입금기록 또는 전자적 장치에의 입력이 끝날 때까지 거래지시를 철회할 수 있습니다.</li>
							<li>회사는 이용자의 거래지시의 철회에 따라 지급거래가 이루어지지 않은 경우 수령한 자금을 이용자에게 반환하여야 합니다.</li>
						</ol>
						<h4 class="term_s_title">제１6조 (접근매체의 관리)</h4>
						<ol>
							<li>회사는 전자지급결제대행 서비스 제공 시 접근매체를 선정하여 이용자의 신원, 권한 및 거래지시의 내용 등을 확인할 수 있습니다.</li>
							<li>이용자는 접근매체를 제 3자에게 대여하거나 그 사용을 위임하거나 양도 또는 담보 목적으로 제공할 수 없습니다.</li>
							<li>이용자는 자신의 접근매체를 제3자에게 누설 또는 노출하거나 방치해서는 안 되며, 접근매체의 도용이나 위조 또는 변조를 방지하기 위하여 충분한 주의를 기울여야 합니다.</li>
							<li>회사는 이용자로부터 접근매체의 분실이나 도난 등의 통지를 받았을 때에는 그때부터 제3자가 그 접근매체를 사용하여 이용자에게 끼친 손해를 배상할 책임이 있습니다.</li>
						</ol>
						<h3 class="term_s_title term_center">제3장   결제대금예치 서비스</h3>
						<h4 class="term_s_title">제１7조 (정의)</h4>
						<p>본 장에서 정하는 용어의 정의는 다음과 같습니다.</p>
						<ol>
							<li>‘결제대금예치서비스’라 함은 몰에서 이루어지는 선불식 통신판매에 있어서, 회사가 소비자가 지급하는 결제대금을 예치하고 배송이 완료된 후 재화 등의 대금을 판매자에게 지급하는 제도를 말합니다.</li>
							<li> ‘선불식 통신판매’라 함은 소비자가 재화 등을 공급받기 전에 미리 대금의 전부 또는 일부를 지급하는 방식의 통신판매를 말합니다.</li>
							<li>‘판매자’라 함은 본 약관에 동의하고 회사 운영의 사이트에 입점한 자로서, 통신판매를 하는 자를 말합니다.</li>
							<li>‘소비자’라 함은 본 약관에 동의하고 회사 운영의 사이트에서 재화 등을 구매하는 자로서 ‘전자상거래 등에서의 소비자보호에 관한 법률’ 제2조 제5호의 요건에 해당하는 자를 말합니다.</li>
							<li>‘이용자’라 함은 ‘판매자’와 ‘소비자’를 말합니다.</li>
						</ol>
						<h4 class="term_s_title">제１8조 (적용범위)</h4>
						<p>본 장은 이용자가 ‘몰’을 통한 선불식 통신판매를 이용하는 경우에 적용됩니다.</p>
						<h4 class="term_s_title">제１9조 (예치된 결제대금의 지급방법)</h4>
						<ol>
							<li>소비자(그 소비자의 동의를 얻은 경우에는 재화 등을 공급받을 자를 포함한다 이하 제2항 및 제3항에서 같습니다)는 재화 등을 공급받은 사실을 재화 등을 공급받은 날부터 3영업일 이내에 회사에 통보하여야 합니다.</li>
							<li>회사는 소비자로부터 재화 등을 공급받은 사실을 통보 받을 경우 회사가 정한 기일 내에 판매자에게 결제대금을 지급합니다.</li>
							<li>회사는 소비자가 재화 등을 공급받은 날로부터 3영업일이 지나도록 정당한 사유의 제시 없이 그 공급받은 사실을 회사에 통보하지 아니하는 경우에 소비자의 동의 없이 판매자에게 결제대금을 지급할 수 있습니다.</li>
							<li>회사가 판매자에게 결제대금을 지급하기 전에 소비자가 그 결제대금을 환급 받을 사유가 발생한 경우에는 그 결제대금을 소비자에게 환급합니다.</li>
